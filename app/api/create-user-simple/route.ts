import { createClient } from "@supabase/supabase-js"
import { NextResponse } from "next/server"

// Cliente normal para enviar Magic Link
const supabase = createClient(process.env.NEXT_PUBLIC_SUPABASE_URL!, process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!)

// Cliente admin para operaciones posteriores
const supabaseAdmin = createClient(process.env.NEXT_PUBLIC_SUPABASE_URL!, process.env.SUPABASE_SERVICE_ROLE_KEY!, {
  auth: {
    autoRefreshToken: false,
    persistSession: false,
  },
})

export async function POST(request: Request) {
  try {
    const { email, name, role = "user", organizationId } = await request.json()

    // Validaci√≥n de entrada
    if (!email || !name || !organizationId) {
      return NextResponse.json({ error: "Faltan datos requeridos." }, { status: 400 })
    }

    // Validar rol
    if (role && !["user", "admin", "coordinador"].includes(role)) {
      return NextResponse.json({ error: "Rol inv√°lido." }, { status: 400 })
    }

    console.log("üîÑ Enviando Magic Link para nuevo usuario:", { email, name, organizationId, role })

    // 1. ENVIAR MAGIC LINK con callback espec√≠fico para invitaciones
    const { data, error } = await supabase.auth.signInWithOtp({
      email: email,
      options: {
        emailRedirectTo: `${process.env.NEXT_PUBLIC_SITE_URL || "http://localhost:3000"}/auth/invite-callback`, // ‚Üê Callback espec√≠fico
        data: {
          full_name: name,
          organization_id: organizationId,
          role: role,
          invite_type: "user_invitation", // ‚Üê Marcador para distinguir
        },
      },
    })

    if (error) {
      console.error("‚ùå Error enviando Magic Link:", error)
      return NextResponse.json({ error: error.message }, { status: 400 })
    }

    console.log("‚úÖ Magic Link enviado exitosamente")
    console.log("üìß Email enviado a:", email)

    return NextResponse.json({
      success: true,
      message: `Magic Link enviado a ${email}. El usuario recibir√° un email para acceder y unirse a la organizaci√≥n.`,
      user: {
        email: email,
        name: name,
        role: role,
        organization_id: organizationId,
      },
    })
  } catch (err: any) {
    console.error("üí• Error general:", err)
    return NextResponse.json({ error: "Error interno del servidor: " + err.message }, { status: 500 })
  }
}
